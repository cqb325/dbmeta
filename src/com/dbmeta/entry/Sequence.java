package com.dbmeta.entry;

public class Sequence {
	private String gsid;
	private String gstitle;
	private int gsseqdatatype;
	private int gsposition;
	public String getGsid() {
		return gsid;
	}
	public void setGsid(String gsid) {
		this.gsid = gsid;
	}
	public String getGstitle() {
		return gstitle;
	}
	public void setGstitle(String gstitle) {
		this.gstitle = gstitle;
	}
	public int getGsseqdatatype() {
		return gsseqdatatype;
	}
	public void setGsseqdatatype(int gsseqdatatype) {
		this.gsseqdatatype = gsseqdatatype;
	}
	public int getGsposition() {
		return gsposition;
	}
	public void setGsposition(int gsposition) {
		this.gsposition = gsposition;
	}
	
}
